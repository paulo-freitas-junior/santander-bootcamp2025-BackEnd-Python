# DECLARANDO CONJUNTOS - Estrutura de dados SET
# Coleção de objetos que não possuem objetos repetidos.
# Obs: NÃO GARANTE A ORDEM dos objetos do conjunto.

# Lista
print(set([1,2,3,1,3,4]))  # 1,2,3,4

# String
print(set('Abacaxi'))   # {'a','c','b','x','i','A'}

# Tupla
print(set(('palio','gol','celta','palio'))) # {'gol','celta','palio'}

