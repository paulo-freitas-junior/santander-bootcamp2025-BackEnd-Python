#   Tipos de dados em Python
#   https://docs.python.org/3/library/stdtypes.html

print(11 + 10 + 1000)  # int

print(11.5 + 10.5 + 1000.5)  # float
print(11 + 10.5 + 1000)  # float

print(True)  # bool
print(False)  # bool

print("Python")  # str

print(11 + 10 + 1000j)  # complex
print(11.5 + 10.5 + 1000j)  # complex

